print("Что-бы начать использовать калькулятор")
a=float(input("Введите первое число: "))
c = input("Введите тип операции(+,-,/,*,**) ")
b=float(input("Введите второе число: "))
#
if c == "+":
    print("Результат: ",a+b)

if c == "-":
    print("Результат: ",a-b)

if c == "*":
    print("Результат: ",a*b)

if c == "/":
    print("Результат: ",a/b)
    
if c == "**":
	print("Результат:",a**b)
